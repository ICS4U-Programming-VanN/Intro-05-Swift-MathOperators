import Foundation

// Addition
let additionResult = 4 + 2
print("4 + 2 = \(additionResult)")

// Subtraction
let subtractionResult = 4 - 2
print("4 - 2 = \(subtractionResult)")

// Multiplication
let multiplicationResult = 4 * 2
print("4 * 2 = \(multiplicationResult)")

// Division (non-decimal)
let divisionResult1 = 4 / 2
print("4 / 2 = \(divisionResult1)")

// Division (infinite decimal)
let divisionResult2 = 2 / 3.0
print("2 / 3 = \(divisionResult2)")

// Exponent to the power of 2
let exponentResult2 = pow(4, 2)
print("4 ^ 2 = \(exponentResult2)")

// Square root
let squareRootResult = sqrt(9)
print("Square root of 9 = \(squareRootResult)")